  <script>
    function handleFacilityNavigation() {
      const hash = window.location.hash;
      if (hash) {
        const targetTabButton = document.querySelector(hash + '-tab');
        const targetSection = document.getElementById('fasilitas');

        if (targetTabButton) {
          // Aktifkan Tab
          const tabTrigger = new bootstrap.Tab(targetTabButton);
          tabTrigger.show();

          // LOGIKA SCROLL:
          // Cek posisi scroll saat ini
          const sectionPos = targetSection.getBoundingClientRect();

          // Jika area fasilitas belum terlihat (masih di atas/jauh di bawah), lakukan scroll
          // Jika sudah di area fasilitas (top antara -100 dan 200), jangan scroll lagi
          if (sectionPos.top > 200 || sectionPos.top < -100) {
            const offset = 100;
            const bodyRect = document.body.getBoundingClientRect().top;
            const elementRect = targetSection.getBoundingClientRect().top;
            const elementPosition = elementRect - bodyRect;
            const offsetPosition = elementPosition - offset;

            window.scrollTo({
              top: offsetPosition,
              behavior: 'smooth'
            });
          }
        }
      }
    }

    // Jalankan saat halaman dimuat
    document.addEventListener('DOMContentLoaded', handleFacilityNavigation);

    // Jalankan saat URL berubah (klik menu saat sudah di profile)
    window.addEventListener('hashchange', handleFacilityNavigation);
  </script>
